fn main() {
    // variable definition
    let is_current = true;  // bool
    let age = 42;  // int
    let area = 42.42;  // float
    let some_words = "Hello, World!";  // string
    
    println!("{is_current}");
    println!("{age}");
    println!("{area}");
    println!("{some_words}");
    
    println!("\nDone.");
}
